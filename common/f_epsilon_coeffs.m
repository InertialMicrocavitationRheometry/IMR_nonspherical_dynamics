function [xi, eta] = f_epsilon_coeffs(R, U, lam, Rddot, n, Ro, We, Re, Ca, alpha, mod)

if mod == "me"
    % pre allocattion for only inertial case
    eta = 4.*(5+2.*n).*U./((4+n).*R);
    xi = 6.*(n+2)./(n+4).*U^2./R^2+(8+n-n.^2).*Rddot./((n+4).*R);

    % --- First-order elastic term (coefficient of G) ---
    elast = 1/(lam^11.*(6+n).*(9+n).*(12+n).*Ro^2).*(1+n).*(2.*lam^5.*(1+ ...
        2.*lam^3-lam^6+n).*(648+234.*n+27.*n^2+n^3)+lam^3.*(-1+lam^3).*(13+ ...
        n).*(-n.*(14+9.*n+n^2)+2.*lam^6.*(54+15.*n+n^2)+lam^3.*n.*(86+ ...
        35.*n+3.*n^2)).*hyp2f1(-(1/3),4+n/3,5+n/3,1-1/lam^3)+(-1+ ...
        lam^3).*(-2.*lam^9.*(54+15.*n+n^2)+lam^6.*n.*(54+69.*n+16.*n^2+ ...
        n^3)+n.*(140+104.*n+19.*n^2+n^3)-lam^3.*n.*(266+199.*n+ ...
        37.*n^2+2.*n^3)).*hyp2f1(2/3,4+n/3,5+n/3,1-1/lam^3));

    % --- Second-order elastic term (coefficient of α.*G) ---
    sselast = (1/(3.*lam^10.*(6+n).*(9+n).*Ro^2)).*(1+n).*(6.*(1-lam^2).*(6+n).*(9+n).*(1+lam^2- ...
        lam^3.*(-4+lam.*(2+lam.*(-4+lam+2.*lam^2+lam^3-2.*lam^5)))-(-2-2.*lam^2+lam^4).*n)-(1/(lam.*(12+ ...
        n))).*3.*(1-lam^3).*(lam^3.*((14+n).*(4.*lam^9.*(6+n).*(9+n)+2.*lam^6.*(1+n).*(2+n).*(6+ ...
        n).*(9+n)+n.*(318+n.*(175+n.*(33+2.*n)))-lam^3.*(-324+n.*(120+n.*(229+n.*(59+ ...
        4.*n))))).*hyp2f1(-(2/3),4+n/3,5+n/3,1-1/lam^3)+3.*(13+n).*(n.*(2+n).*(7+n)- ...
        2.*lam^6.*(6+n).*(9+n)-lam^3.*n.*(86+n.*(35+3.*n))).*hyp2f1(-(1/3),4+n/3,5+n/3,1-1/lam^3))- ...
        2.*(4.*lam^12.*(6+n).*(9+n)+2.*lam^9.*(1+n).*(2+n).*(6+n).*(9+n)+lam^6.*(1+n).*(6+n).*(9+ ...
        n).*(6+n.*(6+n))+n.*(2+n).*(11+n).*(39+n.*(13+n))-lam^3.*n.*(1398+n.*(1411+n.*(427+ ...
        2.*n.*(25+n))))).*hyp2f1(1/3,4+n/3,5+n/3,1-1/lam^3)-3.*(-2.*lam^9.*(6+n).*(9+n)+lam^6.*n.*(1+ ...
        n).*(6+n).*(9+n)+n.*(2+n).*(7+n).*(10+n)-lam^3.*n.*(2+n).*(7+n).*(19+2.*n)).*hyp2f1(2/3,4+n/3,5+n/3,1-1/lam^3)));

    % --- Viscous term (coefficient of μ) for eta ---
    visceta = 2.*(n+1).*(n+2)./R.^2;

    % --- Viscous term (coefficient of μ) for xi ---
    viscxi = (n.*(n+1).*(20+7.*n)).*U./((6+n).*R^3);

    % --- Surface tension term (coefficient of γ) ---
    surften = ((n+1).*(n-1).*(n+2)./R^3);

    % Correcting coefficients
    xi = xi + 1/Ca*elast + alpha/Ca*sselast + 1/(2*We)*surften + 1/Re*viscxi;
    eta = eta + 1/Re*visceta;

elseif mod == "Pros"
    adot_prefact = 3*U/R + 2/R^2*(n+2)*(n+1)./Re;
    aprefact = -(n-1)*Rddot/R +4*(n-1)*(n+1)*U/(Re*R^3)+(n-1)*(n+2)*(n+1)./(2*We*R^3) ...
        +1/(Ca*R^2)*(2*lam^(-1)+2*lam^(-4)+n*(n+1)/(1+lam^(-1)+lam^(-2)));

    xi = aprefact + adot_prefact*U/R + Rddot/R;
    eta = adot_prefact + 2*U/R;    
end

end