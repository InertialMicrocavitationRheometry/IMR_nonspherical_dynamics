function [TCoefKJ, VCoefKJ] = f_bulk_KJ(r, rs, vs, dr, oneW, W, n, R, Rd, Rdd, Ca, Re, alph)
%F_BULK_KJ Build coefficient matrices for the J and K integral terms.

[KmT, JmT] = f_J_K_kernel(r, dr, oneW, W, n);

CJ = -((60.*n.*(2+n).*(3+n).*r.^(-7-n).*R.^2.*Rd)./((1+2.*n).*Re))-(2.*n.*(2+n).*r.^(-12-n).*(3.*(7+n).*r.^4.*R.^4.*Rd.^2.*rs.^7-...
    3.*r.^7.*R.*(2.*Rd.^2+R.*Rdd).*rs.^7))./((1+2.*n).*rs.^7)-1./(Ca.*(1+2.*n).*rs.^7)*2.*n.*(2+...
    n).*r.^(-12-n).*vs.*(r.^4.*rs.^2.*(5.*(3+n).*r.^6-(37+7.*n).*r.^3.*vs+3.*(7+n).*vs.^2)+...
    alph.*(15.*(3+n).*r.^10.*(r-rs).*(r+rs)+r.^7.*((-177+n+4.*n.^2).*r.^2+3.*(37+7.*n).*rs.^2).*vs-...
    r.^4.*(8.*(-36+n.*(3+n)).*r.^2+9.*(7+n).*rs.^2).*vs.^2+(-222+n.*(21+5.*n)).*r.^3.*vs.^3-(-6+n).*(11+n).*vs.^4));
CK = -((60.*(-2+n).*(-1+n.^2).*r.^(-6+n).*R.^2.*Rd)./((1+2.*n).*Re))-(2.*(-1+n.^2).*r.^(-11+n).*(3.*(-6+ ...
    n).*r.^4.*R.^4.*Rd.^2.*rs.^7+3.*r.^7.*R.*(2.*Rd.^2+R.*Rdd).*rs.^7))./((1+2.*n).*rs.^7)-1./(Ca.*(1+2.*n).*rs.^7)*2.*(-1+...
    n.^2).*r.^(-11+n).*vs.*(r.^4.*rs.^2.*(5.*(-2+n).*r.^6+(30-7.*n).*r.^3.*vs+3.*(-6+n).*vs.^2)+alph.*(15.*(-2+n).*r.^10.*(r-...
    rs).*(r+rs)+r.^7.*((174-n.*(7+4.*n)).*r.^2+3.*(-30+7.*n).*rs.^2).*vs+r.^4.*(8.*(-38+(-1+n).*n).*r.^2-9.*(-6+...
    n).*rs.^2).*vs.^2+(238+(11-5.*n).*n).*r.^3.*vs.^3+(-10+n).*(7+n).*vs.^4));


CJd = (6.*n.*(2+n).*r.^(-5-n).*R.^2.*Rd)./(1+2.*n);

CKd = -((6.*(-1+n.^2).*r.^(-4+n).*R.^2.*Rd)./(1+2.*n));

TCoefKJ = diag(CK)*KmT + diag(CJ)*JmT ; 
VCoefKJ = diag(CKd)*KmT + diag(CJd)*JmT;
end
